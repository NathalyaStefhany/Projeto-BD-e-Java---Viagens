    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        int id = Integer.valueOf(jLabelIdDestino.getText());
        String destino = jLabelDestinoViagem.getText();
        
        jTextAreaAltaTemporada.setLineWrap(true);
        jTextAreaClima.setLineWrap(true);
        
        LugaresDAO dao = new LugaresDAO();
        
        Viagens viagem = new Viagens();
        
        viagem = dao.buscarViagem(id);
        
        jTextFieldEntradaDestino.setText(destino);
        jTextAreaAltaTemporada.setText(viagem.getAltaTemporada());
        jTextAreaClima.setText(viagem.getClima());
        
        jTablePontoTuristico.setModel(dao.buscarPontoTuristico(id));
        
        jTablePontoTuristico.getColumnModel().getColumn(0).setMinWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(0).setMaxWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(0).setWidth(0);
        
        this.setIconImage(new ImageIcon("C:/Users/natha/OneDrive/Documentos/NetBeansProjects/Viagens/img/travel.png").getImage());
    }//GEN-LAST:event_formWindowOpened

