    private void jLabelGoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelGoMouseClicked
        int continente = jComboBoxContinente.getSelectedIndex() + 1;
        
        Lugares lugar = new Lugares("", "");
        
        lugar.setDestino(jTextFieldEntradaDestino.getText());
        
        Viagens viagem = new Viagens();
        
        jTextAreaAltaTemporada.setLineWrap(true);
        jTextAreaClima.setLineWrap(true);
        
        viagem.setAltaTemporada(jTextAreaAltaTemporada.getText());
        viagem.setClima(jTextAreaClima.getText());
        
        LugaresDAO dao = new LugaresDAO();
        
        boolean alteracaoViagem = true;
        
        if(!dao.editarViagem(continente, lugar, viagem, Integer.parseInt(jLabelIdDestino.getText()))){
            alteracaoViagem = false;
        }
        
        int linhasPontoTuristico = jTablePontoTuristico.getRowCount();
        
        PontoTuristicoLugares pontoTuristico = new PontoTuristicoLugares();
        
        boolean alteracaoPontoTuristico = true;
        
        for(int i = 0; i < linhasPontoTuristico; i++){
            pontoTuristico.setId(Integer.parseInt(String.valueOf(jTablePontoTuristico.getValueAt(i, 0))));
            pontoTuristico.setNome(String.valueOf(jTablePontoTuristico.getValueAt(i, 1)));
            pontoTuristico.setEndereco(String.valueOf(jTablePontoTuristico.getValueAt(i, 2)));
            pontoTuristico.setPreco(Double.parseDouble(String.valueOf(jTablePontoTuristico.getValueAt(i, 3))));
            PontoTuristicoLugares.latitude = Double.parseDouble(String.valueOf(jTablePontoTuristico.getValueAt(i, 4)).replace(",", "."));
            PontoTuristicoLugares.longitude = Double.parseDouble(String.valueOf(jTablePontoTuristico.getValueAt(i, 5)).replace(",", "."));
            
            if(!dao.editarPontoTuristico(pontoTuristico)){
                alteracaoPontoTuristico = false;
            }
        }
        
        if(alteracaoViagem && alteracaoPontoTuristico){
            new TelaListaLugaresAdmin().setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Erro ao atualizar os dados!");
        }
    }//GEN-LAST:event_jLabelGoMouseClicked

