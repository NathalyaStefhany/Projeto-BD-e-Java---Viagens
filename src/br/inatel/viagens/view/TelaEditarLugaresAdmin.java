    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        if(jTablePontoTuristico.getSelectedRow() != -1){
            LugaresDAO dao = new LugaresDAO();
            
            int linha = jTablePontoTuristico.getSelectedRow();
            
            if(dao.removerPontoTuristico(Integer.parseInt(jTablePontoTuristico.getValueAt(linha, 0).toString()))){
                ((DefaultTableModel)jTablePontoTuristico.getModel()).removeRow(linha);
                
                JOptionPane.showMessageDialog(null, "Remoção realizada com sucesso!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um destino!");
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

