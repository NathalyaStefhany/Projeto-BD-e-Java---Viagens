    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        int id = Integer.valueOf(jLabelIdLugar.getText());
        
        boolean admin = Boolean.parseBoolean(jLabelAdmin.getText());
        
        if(admin){
            jMenuBar.remove(jMenuRoteiro);
           
            jMenuBar.revalidate();
        }
        
        //Faz com que quando chegue no limite do camop de texto quebre a linha
        jTextAreaAltaTemporada.setLineWrap(true);
        jTextAreaClima.setLineWrap(true);
        
        LugaresDAO dao = new LugaresDAO();
        
        Viagens viagem = new Viagens();
        
        viagem = dao.buscarViagem(id);
        
        jTextAreaAltaTemporada.setText(viagem.getAltaTemporada());
        jTextAreaClima.setText(viagem.getClima());
        
        jTablePontoTuristico.setModel(dao.buscarPontoTuristico(Integer.parseInt(jLabelIdLugar.getText())));
        
        jTablePontoTuristico.getColumnModel().getColumn(0).setMinWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(0).setMaxWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(0).setWidth(0);
        
        jTablePontoTuristico.getColumnModel().getColumn(3).setMinWidth(100);
        jTablePontoTuristico.getColumnModel().getColumn(3).setMaxWidth(100);
        jTablePontoTuristico.getColumnModel().getColumn(3).setWidth(100);
        
        jTablePontoTuristico.getColumnModel().getColumn(4).setMinWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(4).setMaxWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(4).setWidth(0);
        
        jTablePontoTuristico.getColumnModel().getColumn(5).setMinWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(5).setMaxWidth(0);
        jTablePontoTuristico.getColumnModel().getColumn(5).setWidth(0);
      
        this.setIconImage(new ImageIcon("C:/Users/natha/OneDrive/Documentos/NetBeansProjects/Viagens/img/travel.png").getImage());
    }//GEN-LAST:event_formWindowOpened

