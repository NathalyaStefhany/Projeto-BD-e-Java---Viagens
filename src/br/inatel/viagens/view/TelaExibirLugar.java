    private void jButtonGoogleMapsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGoogleMapsActionPerformed
        int linha = -1;
        int id;
        
        linha = jTablePontoTuristico.getSelectedRow();
        if(linha == -1)
            JOptionPane.showMessageDialog(null, "Nenhum ponto turístico foi selecionado!", "Erro!", JOptionPane.ERROR_MESSAGE);
        else{
            PontoTuristicoLugares.latitude = Double.parseDouble(String.valueOf(jTablePontoTuristico.getValueAt(linha, 4)).replace(',', '.'));
            PontoTuristicoLugares.longitude = Double.parseDouble(String.valueOf(jTablePontoTuristico.getValueAt(linha, 5)).replace(',', '.'));
        
            new GoogleMaps().setVisible(true);
        }
    }//GEN-LAST:event_jButtonGoogleMapsActionPerformed

