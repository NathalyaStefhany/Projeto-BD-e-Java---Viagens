    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        RoteiroDAO dao = new RoteiroDAO();
        
        int linha = -1;
        int id;
        
        linha = jTableRoteiro.getSelectedRow();
        if(linha == -1)
            JOptionPane.showMessageDialog(null, "Nenhum roteiro foi selecionado!", "Erro!", JOptionPane.ERROR_MESSAGE);
        
        else{ 
            int i = JOptionPane.showConfirmDialog(null, "Deseja excluir o roteiro?", "Excluir", 0);
            
            if(i == 0){
                id = Integer.parseInt(jTableRoteiro.getValueAt(linha, 0).toString());

                if(dao.removerRoteiro(id)){
                    //Remove a linha que foi selecionada
                    ((DefaultTableModel)jTableRoteiro.getModel()).removeRow(linha);
                    
                    JOptionPane.showMessageDialog(null, "RemoÃ§Ã£o realizada com sucesso!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Erro ao remover o roteiro!");
                }
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

