    private void jButtonVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVisualizarActionPerformed
        int linha = -1;
        int id;
        
        //Pega o valor da linha selecionada
        linha = jTableRoteiro.getSelectedRow();
        if(linha == -1)
            JOptionPane.showMessageDialog(null, "Nenhum roteiro foi selecionado!", "Erro!", JOptionPane.ERROR_MESSAGE);
        
        else{ 
            //getValueAt(linha selecionada, coluna que deseja pegar o valor)
            //Como o valor retornado Ã© um objeto, tem que converter esse objeto em String e depois em int
            id = Integer.parseInt(jTableRoteiro.getValueAt(linha, 0).toString());
            
            new TelaExibirRoteiro(id).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonVisualizarActionPerformed

